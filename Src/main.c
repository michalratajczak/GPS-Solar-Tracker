void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
   	if(htim->Instance == TIM2 && MODE == 1)
   	{
   		presentation_mode();
    }

   	if(htim->Instance == TIM9 && MODE == 2)
   	{
   		normal_mode();
    }

   	if(htim->Instance == TIM12)
	{
   		if(TIM4->CCR1 < AZIMUTH)
		{
			TIM4->CCR1++;
		}
		else
		{
			TIM4->CCR1--;
		}

   		if(TIM4->CCR2 < ELEVATION)
		{
			TIM4->CCR2++;
		}
		else
		{
			TIM4->CCR2--;
		}
	}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){

	if(GPIO_Pin == center_Pin && HAL_GPIO_ReadPin(GPIOE, center_Pin) == 1)
	  {
		  MODE = 0;
		  AZIMUTH = set_servo_azimuth(0);
		  ELEVATION = set_servo_elevation(0);
 			presentation_mode_counter = 0;
	  }
	  else if(GPIO_Pin == presentation_Pin && HAL_GPIO_ReadPin(GPIOE, presentation_Pin) == 1)
	  {
		  MODE = 1;
 			presentation_mode();
	  }
	  else if(GPIO_Pin == normal_Pin && HAL_GPIO_ReadPin(GPIOE, normal_Pin) == 1)
	  {
		  MODE = 2;
 			presentation_mode_counter = 0;
 			normal_mode();
	  }
}

